/* 
    Switch Case
*/

var opcao = parseInt(prompt('Digite um número de 1 a 5: '));

switch(opcao){

    case 1:
        document.write('Opção 1');
        break;

    case 2:
        document.write('Opção 2');
        break;

    case 3:
        document.write('Opção 3');
        break;

    case 4:
        document.write('Opção 4');
        break;

    case 5:
        document.write('Opção 5');
        break;

    default:
        document.write('Opção Inválida!')
}